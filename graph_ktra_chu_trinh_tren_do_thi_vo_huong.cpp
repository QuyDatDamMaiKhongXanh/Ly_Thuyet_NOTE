using namespace std;
ll n, m, degree[1001], visited[1001] = {0}, parent[1001] = {0};
vector<ll> adj[1001], toco;
void init()
{
    cin >> n >> m;
    for (ll i = 0; i < n; i++)
    {
        ll x, y;
        cin >> x >> y;
        degree[y]++;
        adj[x].pb(y);
        adj[y].pb(x);
    }
    for (ll i = 1; i <= n; i++)
    {
        sort(all(adj[i]));
    }
}
bool dfs(int i)
{
    visited[i] = 1;
    for (auto x : adj[i])
    {
        if (!visited[i])
        {
            parent[x] = i;
            if (dfs(x))
                return true;
        }
        else if (x != parent[i])
            return true;
    }
    return false;
}
bool bfs(int i)
{
    queue<int> qe;
    qe.push(i);
    visited[i] = 1;
    while (!qe.empty())
    {
        int top = qe.front();
        qe.pop();
        for (auto x : adj[top])
        {
            if (!visited[x])
            {
                qe.push(x);
                visited[x] = 1;
                parent[x] = top;
            }
            else if (x != parent[top])
                return true;
        }
    }
    return false;
}
